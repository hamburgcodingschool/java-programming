package com.hamburgcodingschool.session1;

public class Loops {

    public static void main(String[] args) {
        for (int i = 0; i < 100; i++) {
            System.out.println(i);
        }

        int i = 0;
        while (i < 100) {
            System.out.println(i);
            i++;
        }
    }
}
